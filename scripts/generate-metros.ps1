# Script to generate static metro-area data from data/metro-areas.json
#
# The curated source of truth is data/metro-areas.json. This script emits:
#   - src/ShowsOnSale.World/Data/Metros/<Slug>.cs  (one file per metro area)
#   - src/ShowsOnSale.World/WorldData.Metros.cs     (the WorldData.MetroAreas registry)
#
# Unlike generate-world.ps1, this script does NOT touch the git submodule — metro areas
# are a curated overlay that the upstream countries-states-cities database does not provide.

param(
    [string]$InputPath = "$PSScriptRoot/../data/metro-areas.json",
    [string]$OutputRoot = "$PSScriptRoot/../src/ShowsOnSale.World"
)

$ErrorActionPreference = "Stop"

$generatedHeader = @"
// ⚠️ WARNING: This file is automatically generated by scripts/generate-metros.ps1
// DO NOT modify this file manually as it will be overwritten when the script is run again.
// To update the data, modify data/metro-areas.json and run the script.
"@

# Convert a metro id slug (e.g. "us-nyc") into a PascalCase C# class name (e.g. "UsNyc").
function Get-ClassName {
    param([string]$id)
    $words = ($id -replace '[^a-zA-Z0-9]', ' ') -split '\s+' | Where-Object { $_ -ne '' }
    $pascal = $words | ForEach-Object { $_.Substring(0, 1).ToUpper() + $_.Substring(1).ToLower() }
    $name = ($pascal -join '')
    if ($name -match '^[0-9]') { $name = "Metro$name" }
    return $name
}

# Escape a string for embedding in a C# verbatim-free string literal.
function Get-EscapedString {
    param([string]$text)
    if ($null -eq $text) { return $null }
    return $text.Replace('\', '\\').Replace('"', '\"')
}

# Render a C# literal for a nullable string property value.
function Get-StringLiteral {
    param($value)
    if ($null -eq $value) { return 'null' }
    return '"' + (Get-EscapedString $value) + '"'
}

# Render a C# literal for a nullable int property value.
function Get-IntLiteral {
    param($value)
    if ($null -eq $value) { return 'null' }
    return [string]([int]$value)
}

$json = Get-Content -Raw -Path $InputPath | ConvertFrom-Json

$metrosDir = Join-Path $OutputRoot "Data/Metros"
if (-not (Test-Path $metrosDir)) { New-Item -ItemType Directory -Path $metrosDir | Out-Null }

$registryEntries = @()

foreach ($metro in $json.metroAreas) {
    $className = Get-ClassName $metro.id
    $registryEntries += "            ShowsOnSale.World.Data.Metros.$className.Data,"

    $countries = ($metro.countries | ForEach-Object { '"' + (Get-EscapedString $_) + '"' }) -join ', '

    $memberLines = @()
    foreach ($m in $metro.members) {
        $parts = @(
            "Type = MetroMemberType.$($m.type)",
            "CountryIso2 = $(Get-StringLiteral $m.country)",
            "StateId = $(Get-IntLiteral $m.stateId)",
            "StateName = $(Get-StringLiteral $m.stateName)",
            "CityId = $(Get-IntLiteral $m.cityId)",
            "Name = $(Get-StringLiteral $m.name)"
        )
        $memberLines += "                new() { " + ($parts -join ', ') + " }"
    }
    $membersBlock = $memberLines -join ",`r`n"

    $file = @"
$generatedHeader

using System.Collections.Generic;
using ShowsOnSale.World.Models;

namespace ShowsOnSale.World.Data.Metros
{
    public static class $className
    {
        public static MetroArea Data { get; } = new()
        {
            Id = $(Get-StringLiteral $metro.id),
            Name = $(Get-StringLiteral $metro.name),
            ShortName = $(Get-StringLiteral $metro.shortName),
            CountryIso2 = $(Get-StringLiteral $metro.primaryCountry),
            Countries = new() { $countries },
            Code = $(Get-StringLiteral $metro.code),
            Latitude = $(Get-StringLiteral $metro.latitude),
            Longitude = $(Get-StringLiteral $metro.longitude),
            Members = new()
            {
$membersBlock
            }
        };
    }
}
"@

    $outFile = Join-Path $metrosDir "$className.cs"
    Set-Content -Path $outFile -Value $file -Encoding UTF8
    Write-Host "Generated $outFile"
}

$registryBlock = $registryEntries -join "`r`n"
$registry = @"
$generatedHeader

using System.Collections.Generic;
using ShowsOnSale.World.Models;

namespace ShowsOnSale.World
{
    public static partial class WorldData
    {
        /// <summary>
        /// All metro areas. A metro area groups one or more cities, counties, and/or states,
        /// and may cross state and country borders.
        /// </summary>
        public static List<MetroArea> MetroAreas { get; } = new()
        {
$registryBlock
        };
    }
}
"@

Set-Content -Path (Join-Path $OutputRoot "WorldData.Metros.cs") -Value $registry -Encoding UTF8
Write-Host "Generated WorldData.Metros.cs with $($json.metroAreas.Count) metro area(s)."
