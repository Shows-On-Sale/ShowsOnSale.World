// Emits embedded C# from the curated JSON (data/metro-areas.json + combined-statistical-areas.json).
// Mirrors scripts/generate-metros.ps1 output. Run: node scripts/gen-cs.js
const fs = require('fs'), path = require('path');
const ROOT = path.join(__dirname, '..');
const SRC = path.join(ROOT, 'src/ShowsOnSale.World');
const HEADER = `// ⚠️ WARNING: This file is automatically generated by scripts/generate-metros.ps1
// DO NOT modify this file manually as it will be overwritten when the script is run again.
// To update the data, modify data/metro-areas.json and run the script.`;
const cn = id => id.split(/[^a-zA-Z0-9]/).filter(Boolean).map(w => w[0].toUpperCase() + w.slice(1).toLowerCase()).join('');
const str = v => v == null ? 'null' : '"' + String(v).replace(/\\/g, '\\\\').replace(/"/g, '\\"') + '"';
const intv = v => v == null ? 'null' : String(v);
const wipe = dir => { fs.mkdirSync(dir, { recursive: true }); for (const f of fs.readdirSync(dir)) if (f.endsWith('.cs')) fs.unlinkSync(path.join(dir, f)); };

const metros = JSON.parse(fs.readFileSync(path.join(ROOT, 'data/metro-areas.json'), 'utf8')).metroAreas;
const csas = JSON.parse(fs.readFileSync(path.join(ROOT, 'data/combined-statistical-areas.json'), 'utf8')).combinedStatisticalAreas;

// metros
const MD = path.join(SRC, 'Data/Metros'); wipe(MD);
const mReg = [];
for (const m of metros) {
  const c = cn(m.id);
  mReg.push(`            ShowsOnSale.World.Data.Metros.${c}.Data,`);
  const countries = m.countries.map(str).join(', ');
  const members = m.members.map(mm =>
    `                new() { Type = MetroMemberType.${mm.type}, CountryIso2 = ${str(mm.country)}, StateId = ${intv(mm.stateId)}, StateName = ${str(mm.stateName)}, CityId = ${intv(mm.cityId)}, Name = ${str(mm.name)} }`
  ).join(',\n');
  fs.writeFileSync(path.join(MD, c + '.cs'), `${HEADER}

using System.Collections.Generic;
using ShowsOnSale.World.Models;

namespace ShowsOnSale.World.Data.Metros
{
    public static class ${c}
    {
        public static MetroArea Data { get; } = new()
        {
            Id = ${str(m.id)},
            Name = ${str(m.name)},
            ShortName = ${str(m.shortName)},
            CountryIso2 = ${str(m.primaryCountry)},
            Countries = new() { ${countries} },
            Code = ${str(m.code)},
            Type = MetroAreaType.${m.type || 'Other'},
            Population = ${intv(m.population)},
            CsaId = ${str(m.csaId)},
            Latitude = ${str(m.latitude)},
            Longitude = ${str(m.longitude)},
            Members = new()
            {
${members}
            }
        };
    }
}
`);
}
fs.writeFileSync(path.join(SRC, 'WorldData.Metros.cs'), `${HEADER}

using System.Collections.Generic;
using ShowsOnSale.World.Models;

namespace ShowsOnSale.World
{
    public static partial class WorldData
    {
        /// <summary>
        /// All metro areas. A metro area groups one or more cities, counties, and/or states,
        /// and may cross state and country borders.
        /// </summary>
        public static List<MetroArea> MetroAreas { get; } = new()
        {
${mReg.join('\n')}
        };
    }
}
`);

// CSAs
const CD = path.join(SRC, 'Data/Csas'); wipe(CD);
const cReg = [];
for (const c of csas) {
  const k = cn(c.id);
  cReg.push(`            ShowsOnSale.World.Data.Csas.${k}.Data,`);
  const ids = c.metroIds.map(str).join(', ');
  fs.writeFileSync(path.join(CD, k + '.cs'), `${HEADER}

using System.Collections.Generic;
using ShowsOnSale.World.Models;

namespace ShowsOnSale.World.Data.Csas
{
    public static class ${k}
    {
        public static CombinedStatisticalArea Data { get; } = new()
        {
            Id = ${str(c.id)},
            Name = ${str(c.name)},
            Code = ${str(c.code)},
            CountryIso2 = ${str(c.country)},
            Population = ${intv(c.population)},
            MetroIds = new() { ${ids} }
        };
    }
}
`);
}
fs.writeFileSync(path.join(SRC, 'WorldData.Csa.cs'), `${HEADER}

using System.Collections.Generic;
using ShowsOnSale.World.Models;

namespace ShowsOnSale.World
{
    public static partial class WorldData
    {
        /// <summary>
        /// All combined statistical areas (CSAs) — groupings of adjacent metro areas linked by
        /// commuting ties. Each references its member metros by id via <see cref="CombinedStatisticalArea.MetroIds"/>.
        /// </summary>
        public static List<CombinedStatisticalArea> CombinedStatisticalAreas { get; } = new()
        {
${cReg.join('\n')}
        };
    }
}
`);
console.log('emitted metros:', metros.length, '| csas:', csas.length);
